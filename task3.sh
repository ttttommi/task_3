# Завантажуємо псевдоніми
source ~/.bashrc

# Перевірка на наявність двох параметрів
if [ "$#" -ne 2 ]; then
  echo "Скрипт потребує два числові параметри."
  exit 1
fi

# Перевірка, чи параметри є числами
if ! [[ "$1" =~ ^-?[0-9]+$ && "$2" =~ ^-?[0-9]+$ ]]; then
  echo "Будь ласка, введіть два числові параметри."
  exit 1
fi

# Зберігаємо параметри
num1=$1
num2=$2

# Назва скрипта
script_name=$(basename "$0")

# Порівняння чисел
if [ "$num1" -gt "$num2" ]; then
  echo "Перший параметр ($num1) більше другого ($num2)."
  echo "Список псевдонімів системи:"
  
  # Перевірка на наявність псевдонімів
  if alias | grep -q 'alias'; then
    alias
  else
    echo "Псевдонімів не знайдено."
  fi

else
  echo "Перший параметр ($num1) не більше другого ($num2)."
  echo "Розмір файлу скрипта ($script_name):"
  stat --format="%s байтів" "$0"
fi
